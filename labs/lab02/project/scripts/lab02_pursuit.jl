mkpath(plotsdir(script_name))
mkpath(datadir(script_name))

# ## 1. Определение начального этапа движения
#
# ### Вывод уравнения для точки перехода
#
# В момент обнаружения лодка в полюсе (θ), катер на расстоянии $R$.
# Рассмотрим два случая начального положения катера относительно полюса
# (линии, по которой уйдет лодка).
#
# **Случай 1 (самый вероятный):** Катеру нужно двигаться **к** полюсу,
# чтобы сократить расстояние. Пусть через время $t$ они окажутся на одном
# расстоянии $x$ от полюса. Лодка пройдет $x$ со скоростью $v$, катер
# пройдет $R - x$ со скоростью $n \cdot v$. Время одинаково:
# $$ \frac{x}{v} = \frac{R - x}{n v} $$
# Решая уравнение, получаем $x = \frac{R}{n+1}$.
#
# **Случай 2 (менее вероятный):** Лодка уходит в направлении,
# **противоположном** катеру. Тогда катеру нужно двигаться **от** полюса,
# чтобы догнать лодку. Катер пройдет $R + x$, лодка $x$.
# $$ \frac{x}{v} = \frac{R + x}{n v} $$
# Решая уравнение, получаем $x = \frac{R}{n-1}$.
#
# > **Примечание:** Во втором случае решение существует только если $n > 1$,
# > что выполняется по условию задачи. Если $n \le 1$, преследование
# > бессмысленно.
#
# ### Расчет точки перехода для нашего варианта
#
# Подставим значения $R = 9$ км и $n = 3$.
R = 9.0
n = 3.0

x1 = R / (n + 1)
x2 = R / (n - 1)

println("="^60)
println("РЕЗУЛЬТАТЫ РАСЧЕТА ТОЧКИ ПЕРЕХОДА")
println("="^60)
println("Случай 1 (движение к полюсу): x₁ = R/(n+1) = $(round(x1, digits=2)) км")
println("Случай 2 (движение от полюса): x₂ = R/(n-1) = $(round(x2, digits=2)) км")
println("")

# ## 2. Математическая модель движения катера после перехода
#
# После того как катер оказался на одном расстоянии $x$ от полюса с лодкой,
# он начинает двигаться вокруг полюса, удаляясь от него со скоростью лодки
# $v$. Разложим скорость катера $v_k = n \cdot v$ на радиальную
# $v_r = dr/dt$ и тангенциальную $v_\tau = r \cdot d\theta/dt$ составляющие.
#
# По условию стратегии, радиальная скорость катера должна равняться скорости
# лодки: $v_r = v$. Тогда из теоремы Пифагора для скоростей:
# $$ v_\tau = \sqrt{v_k^2 - v_r^2} = \sqrt{(n v)^2 - v^2} = v\sqrt{n^2 - 1} $$
#
# Подставляя выражения для $v_r$ и $v_\tau$, получаем систему
# дифференциальных уравнений, описывающих траекторию катера в полярных
# координатах $(r, \theta)$:
#
# \[
# \begin{cases}
# \frac{dr}{dt} = v \\
# r \frac{d\theta}{dt} = v\sqrt{n^2 - 1}
# \end{cases}
# \]
#
# Исключая время $dt$, получаем дифференциальное уравнение траектории:
# \[ \frac{dr}{d\theta} = \frac{r}{\sqrt{n^2 - 1}} \]
#
# ### Решение уравнения траектории
#
# Решим это уравнение аналитически. Это уравнение с разделяющимися
# переменными:
# $$ \frac{dr}{r} = \frac{d\theta}{\sqrt{n^2 - 1}} $$
#
# Интегрируя, получаем:
# $$ \ln r = \frac{\theta}{\sqrt{n^2 - 1}} + C $$
# $$ r(\theta) = r_0 \cdot e^{\theta / \sqrt{n^2 - 1}} $$
#
# где $r_0 = x$ — начальное расстояние, с которого катер начинает движение
# по спирали (точка перехода), а $\theta$ отсчитывается от направления на
# катер в этот момент.
#
# ### Начальные условия для двух случаев
#
# Начальный угол $\theta_0$ зависит от того, с какой стороны от лодки
# находился катер. В полярных координатах, где лодка в полюсе, а полярная ось
# направлена на катер в начальный момент обнаружения:
#
# * **Случай 1 (движение к полюсу):** Катер движется прямолинейно к полюсу,
#   и в момент начала спирали ($r = x_1$) его угол $\theta = 0$.
# * **Случай 2 (движение от полюса):** Катер движется прямолинейно от полюса,
#   и в момент начала спирали ($r = x_2$) он находится в противоположном
#   направлении, т.е. его угол $\theta = \pi$.

# ## 3. Численное моделирование траекторий
#
# Поскольку лодка уходит в неизвестном направлении, для гарантированного
# перехвата катер должен двигаться по спирали. Точка пересечения траекторий
# будет найдена, когда угол поворота катера $\theta$ совпадет с направлением
# движения лодки. Мы промоделируем оба случая и построим траектории.

# ### Параметры модели
#
# Определим константы и параметры для численного решения.
# Скорость лодки для простоты примем за 1 (безразмерную единицу).
v_boat = 1.0
v_guard = n * v_boat

# Параметр для уравнения траектории
k = sqrt(n^2 - 1)

# ### Функция для решения ДУ траектории
#
# Определим функцию для правой части нашего уравнения $dr/d\theta = r/k$.
function trajectory_eq!(dr, r, p, θ)
    k = p
    dr[1] = r[1] / k
end

# ### Моделирование для Случая 1
#
# Начальные условия для случая 1:
# $r_0 = x_1$, $\theta_0 = 0$.
# Будем решать уравнение до достаточно большого угла $\theta_{max}$.
r0_1 = x1
θ_span_1 = (0.0, 4π)  # Пройдем два полных оборота для наглядности
prob1 = ODEProblem(trajectory_eq!, [r0_1], θ_span_1, k)
sol1 = solve(prob1, abstol=1e-8, reltol=1e-8)

# Преобразуем решение в декартовы координаты для построения графика.
θ_coord_1 = sol1.t
r_coord_1 = [sol1.u[i][1] for i in 1:length(sol1.u)]
x_traj_1 = r_coord_1 .* cos.(θ_coord_1)
y_traj_1 = r_coord_1 .* sin.(θ_coord_1)

# ### Моделирование для Случая 2
#
# Начальные условия для случая 2:
# $r_0 = x_2$, $\theta_0 = \pi$.
r0_2 = x2
θ_span_2 = (π, 5π) # Начинаем с π
prob2 = ODEProblem(trajectory_eq!, [r0_2], θ_span_2, k)
sol2 = solve(prob2, abstol=1e-8, reltol=1e-8)

# Преобразуем решение в декартовы координаты.
θ_coord_2 = sol2.t
r_coord_2 = [sol2.u[i][1] for i in 1:length(sol2.u)]
x_traj_2 = r_coord_2 .* cos.(θ_coord_2)
y_traj_2 = r_coord_2 .* sin.(θ_coord_2)

# ## 4. Моделирование движения лодки и поиск точки встречи
#
# Лодка движется прямолинейно в неизвестном направлении. Предположим,
# что она уходит под некоторым фиксированным углом $\phi$ к полярной оси.
# Ее траектория в декартовых координатах: $x_{boat}(t) = v_l \cdot t \cdot \cos(\phi)$,
# $y_{boat}(t) = v_l \cdot t \cdot \sin(\phi)$.
#
# Точка встречи — это точка, где координаты катера и лодки совпадают.
# Для нашего моделирования выберем конкретный угол, например, $\phi = \pi/4$
# (45 градусов), и найдем точку пересечения аналитически или графически.

ϕ = π/4

# Функция для траектории лодки
boat_x(t) = v_boat * t * cos(ϕ)
boat_y(t) = v_boat * t * sin(ϕ)

# ### Аналитический расчет точки встречи для Случая 1
#
# В точке встречи выполняются два условия:
# 1. $r(\theta) = x_1 \cdot e^{\theta/k} = v_l \cdot t$
#    (катер и лодка на одном расстоянии от полюса)
# 2. Угол катера $\theta$ равен углу лодки $\phi$ (направление на лодку).
#
# Подставляя $\theta = \phi$ в первое уравнение, получаем расстояние до точки
# встречи: $r_{meet} = x_1 \cdot e^{\phi / k}$.
#
# Время до встречи: $t_{meet} = r_{meet} / v_l$.

t_meet_1 = x1 * exp(ϕ / k) / v_boat
r_meet_1 = v_boat * t_meet_1
x_meet_1 = r_meet_1 * cos(ϕ)
y_meet_1 = r_meet_1 * sin(ϕ)

println("РЕЗУЛЬТАТЫ ДЛЯ СЛУЧАЯ 1")
println("-"^40)
println("Точка перехода (x₁): $(round(x1, digits=2)) км")
println("Параметр спирали (k = √(n²-1)): $(round(k, digits=2))")
println("Аналитическая точка встречи при φ = $(round(ϕ, digits=2)) рад:")
println("  Координаты: x = $(round(x_meet_1, digits=2)) км, y = $(round(y_meet_1, digits=2)) км")
println("  Расстояние от полюса: $(round(r_meet_1, digits=2)) км")
println("  Время встречи: $(round(t_meet_1, digits=2)) ед. времени")
println("")

# ### Аналитический расчет точки встречи для Случая 2
#
# Аналогично, но с учетом начального угла $\theta_0 = \pi$.
# В точке встречи угол катера $\theta = \phi$ (или $\phi + 2\pi$, но мы
# ищем первое пересечение). Наше решение начинается с $\pi$, поэтому
# нужно найти угол $\theta_{meet}$, который равен $\phi$ по модулю $2\pi$.
# Если $\phi < \pi$, то катер должен пройти через угол $\phi + 2\pi$,
# чтобы его траектория пересекла луч лодки.
#
# Примем $\theta_{meet} = \phi + 2\pi$, тогда:
θ_meet_2 = ϕ + 2π
r_meet_2 = x2 * exp((θ_meet_2 - π) / k)  # (θ - θ₀)
t_meet_2 = r_meet_2 / v_boat
x_meet_2 = r_meet_2 * cos(ϕ)
y_meet_2 = r_meet_2 * sin(ϕ)

println("РЕЗУЛЬТАТЫ ДЛЯ СЛУЧАЯ 2")
println("-"^40)
println("Точка перехода (x₂): $(round(x2, digits=2)) км")
println("Аналитическая точка встречи при φ = $(round(ϕ, digits=2)) рад:")
println("  Угол встречи катера θ_meet = $(round(θ_meet_2, digits=2)) рад ($(round(θ_meet_2/π, digits=2))π)")
println("  Координаты: x = $(round(x_meet_2, digits=2)) км, y = $(round(y_meet_2, digits=2)) км")
println("  Расстояние от полюса: $(round(r_meet_2, digits=2)) км")
println("  Время встречи: $(round(t_meet_2, digits=2)) ед. времени")

# ## 5. Визуализация траекторий
#
# Построим траектории движения катера для обоих случаев и лодки,
# чтобы наглядно увидеть точку пересечения.

# Траектория лодки (достаточно большого отрезка времени)
t_boat_max = max(t_meet_1, t_meet_2) * 1.2
t_boat_range = range(0, t_boat_max, length=200)
x_boat = boat_x.(t_boat_range)
y_boat = boat_y.(t_boat_range)

# ### График для Случая 1
plt1 = plot(title="Случай 1: Катер движется к полюсу (x₁ = $(round(x1, digits=2)) км)",
            xlabel="x (км)", ylabel="y (км)", aspect_ratio=:equal, legend=:topleft,
            grid=true, size=(800, 600))

# Точка обнаружения (полюс)
scatter!(plt1, [0], [0], label="Лодка в t=0 (полюс)", markershape=:circle, markersize=8, color=:black)
# Начальное положение катера
scatter!(plt1, [R], [0], label="Катер в t=0 (R, 0)", markershape=:square, markersize=8, color=:blue)
# Точка перехода
scatter!(plt1, [x1], [0], label="Точка перехода (x₁, 0)", markershape=:diamond, markersize=6, color=:green)

# Траектория лодки
plot!(plt1, x_boat, y_boat, label="Траектория лодки (φ = π/4)", linewidth=2, color=:red, linestyle=:dash)
# Траектория катера
plot!(plt1, x_traj_1, y_traj_1, label="Траектория катера (спираль)", linewidth=2, color=:blue)

# Точка встречи
scatter!(plt1, [x_meet_1], [y_meet_1], label="Точка встречи", markershape=:star5, markersize=10, color=:orange)

# ### График для Случая 2
plt2 = plot(title="Случай 2: Катер движется от полюса (x₂ = $(round(x2, digits=2)) км)",
            xlabel="x (км)", ylabel="y (км)", aspect_ratio=:equal, legend=:topleft,
            grid=true, size=(800, 600))

# Точка обнаружения (полюс)
scatter!(plt2, [0], [0], label="Лодка в t=0 (полюс)", markershape=:circle, markersize=8, color=:black)
# Начальное положение катера
scatter!(plt2, [-R], [0], label="Катер в t=0 (-R, 0)", markershape=:square, markersize=8, color=:blue)
# Точка перехода (отрицательная X, так как угол π)
scatter!(plt2, [x2*cos(π)], [x2*sin(π)], label="Точка перехода (θ=π)", markershape=:diamond, markersize=6, color=:green)

# Траектория лодки
plot!(plt2, x_boat, y_boat, label="Траектория лодки (φ = π/4)", linewidth=2, color=:red, linestyle=:dash)
# Траектория катера
plot!(plt2, x_traj_2, y_traj_2, label="Траектория катера (спираль)", linewidth=2, color=:blue)

# Точка встречи
scatter!(plt2, [x_meet_2], [y_meet_2], label="Точка встречи", markershape=:star5, markersize=10, color=:orange)

