# # Преследование браконьеров: Анализ траектории перехвата
#
# **Цель работы:** Исследовать траекторию движения катера береговой охраны,
# преследующего браконьерскую лодку, и определить точку их встречи.
#
# ## Постановка задачи
#
# В момент обнаружения ($t_0 = 0$) лодка браконьеров находится в точке
# полюса полярной системы координат ($r = 0$). Катер береговой охраны
# находится на расстоянии $R = 9$ км от лодки. Известно, что скорость катера
# $v_k$ в $n = 3$ раза превышает скорость лодки $v_l$: $v_k = n \cdot v_l$.
#
# Лодка уходит прямолинейно в неизвестном направлении. Задача катера —
# выбрать такую траекторию, которая гарантированно приведет к встрече с
# лодкой. Стратегия катера делится на два этапа:
# 1. **Прямолинейное движение** к некоторой точке, чтобы оказаться на одном
#    расстоянии от полюса (точки обнаружения лодки), что и лодка.
# 2. **Движение по спирали**, где радиальная скорость катера ($v_r$) равна
#    скорости лодки, а тангенциальная скорость ($v_\tau$) обеспечивает
#    движение по окружности с увеличивающимся радиусом.
#
# ## Инициализация проекта и загрузка пакетов
#
# Следуя методологии, активируем проект DrWatson.

